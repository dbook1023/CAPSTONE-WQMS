function getStoredDownloadCount() {
    const stored = Number(localStorage.getItem('wqms_report_download_count'));
    return Number.isFinite(stored) && stored >= 0 ? stored : 0;
}

function setStoredDownloadCount(value) {
    localStorage.setItem('wqms_report_download_count', String(Math.max(0, value)));
}

async function fetchUserReports() {
    try {
        // Fetch the report list and system settings without relying on the unavailable analytics route.
        const [dbReports, settings] = await Promise.all([
            API.reports.getAll(),
            API.settings.getAll()
        ]);

        globalReports = dbReports;

        // Map reports dynamically to the UI list
        const reports = dbReports.map(r => ({
            id: r.id,
            title: `Compliance Report #${r.id}: ${r.fountain_name}`,
            description: `Generated by ${r.generated_by_name || 'Admin'} on ${parseBackendDate(r.created_at).toLocaleDateString('en-PH', { timeZone: 'Asia/Manila' })} for ${r.location}`,
            file_size: `Result: ${r.overall_status} • ${r.readings_count} Samples Captured`,
            status: r.overall_status,
            raw: r
        }));

        renderReportStats(reports, settings);
        renderReportsList(reports);
    } catch (error) {
        console.error('Failed to fetch reports:', error);
        showNotification('Could not load water quality reports list from server.', 'error');
    }
}

function deriveNextAuditValue(data, settings = {}) {
    const settingValue = settings.next_audit_date || settings.next_audit || settings.nextAudit;
    if (settingValue) {
        return formatPhilippineDateTime(settingValue, {
            month: 'short',
            day: 'numeric',
            year: 'numeric'
        });
    }

    if (data.length > 0) {
        const latestReport = data[0]?.raw?.created_at;
        if (latestReport) {
            const nextAuditDate = new Date(parseBackendDate(latestReport));
            if (!Number.isNaN(nextAuditDate.getTime())) {
                nextAuditDate.setDate(nextAuditDate.getDate() + 30);
                return formatPhilippineDateTime(nextAuditDate, {
                    month: 'short',
                    day: 'numeric',
                    year: 'numeric'
                });
            }
        }
    }

    return '--';
}

function renderReportStats(data, settings = {}) {
    const totalReports = data.length;
    const totalDownloads = getStoredDownloadCount();
    const nextAudit = deriveNextAuditValue(data, settings);

    const totalVal = document.getElementById('availableReportsValue');
    if (totalVal) totalVal.textContent = String(totalReports);

    const downloadVal = document.getElementById('reportDownloadsValue');
    if (downloadVal) downloadVal.textContent = totalDownloads.toString();

    const nextAuditVal = document.getElementById('nextAuditValue');
    if (nextAuditVal) nextAuditVal.textContent = nextAudit;
}

